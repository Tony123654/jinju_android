package com.jinju.android.api;

public class Category {

	private String mCode;
	private String mName;

	public String getCode() {
		return mCode;
	}

	public void setCode(String code) {
		mCode = code;
	}

	public String getName() {
		return mName;
	}

	public void setName(String name) {
		mName = name;
	}

}